#! /bin/bash

if [ "$UID" -ne "0" ]
then
  echo "You are not root. Exiting.."
  exit 1
fi


echo "========================================================================================"
echo "============== [ IP Configuration ]====================================================="
cat /etc/network/interfaces

echo "========================================================================================"
echo "============= [ RSyslog Configuration ] ================================================"
cat /etc/rsyslog.conf

echo "========================================================================================"
echo "============= [ Logs ] ================================================================="
cat /var/log/syslog | tail -n 5

